# 1. 从数目比较小的情形开始考虑:
#   采用二分的思想解决问题
#   * 对于两瓶药水，只需要一只老鼠就可以检验有毒的是哪一瓶药
#   * 对于四瓶药水，需要两只老鼠检验（方法为：对四瓶药编号为：1，2，3，4。两只老鼠分别和1，2与
#   2，3的药水）
#   * 对于8瓶药水则需要3只老鼠（老鼠的喝药方案为：第一只：5678，第二只：3478，第三只：2468）
# 2. 寻找规律
#   * 由上述归纳总结得出 n只老鼠最多可以判别2^n次方瓶药水
#   * 将每个老鼠对每瓶药的饮用情况（喝了用1表示，没喝用0表示）,可对每个药瓶进行编码（如000，001等）
#   * 通过上述编码再根据每只老鼠的存活条件蝌构造一颗满二叉树，从而对每瓶药的情况进行判断
# 3. 计算结果
#   * 求出log(1000, 2) + 1)得出答案为10